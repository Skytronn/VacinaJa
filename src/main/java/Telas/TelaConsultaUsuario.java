    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTUsuarios.getModel();
        
        UsuarioDAO usuario = new UsuarioDAO();
        
        for(Usuario u: usuario.lerUsuarios()){
            
            modelo.addRow(new Object[]{
                u.getNome(),
                u.getCpf(),
                u.getPerfil()
            });
            
        }
        
    }
